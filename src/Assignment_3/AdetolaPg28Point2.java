package Assignment_3;

//Question: Write a program in Java to display the first 10 natural numbers in descending
//order using do..while loop.

public class AdetolaPg28Point2 {

	public static void main(String[] args) {
		
		int i=10;  	//This is the method when using the Do While loop
		do { 
			System.out.println(i);
			i--;
		} while (i>=1); 
		
	}

}
