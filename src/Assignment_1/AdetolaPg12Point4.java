package Assignment_1;
// Write a Java program to swap two numbers.

public class AdetolaPg12Point4 {

	public static void main(String[] args) {
		
		int X=8; 
		int Y=5;
		
		int temp;
		
		temp=X;
		X=Y;
		Y=temp;
		
		System.out.println("To swap between two numbers : X = "+ X +" and Y = "+ Y );
	}

}
