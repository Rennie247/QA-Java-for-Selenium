package Week8_HW;

public class even_oddNumbers {

	public static void main(String[] args) {
		// Homework week 8; Number 3
		

	for (int num=1; num<=20; num++) {    
   
		if ( num % 2 == 0 )
			System.out.println(+num+ " is an even number");
		else
			System.out.println(+num+ " is an odd number");
		}
	}
}
